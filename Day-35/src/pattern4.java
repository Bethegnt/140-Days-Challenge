public class pattern4 {
    public static void main(String[] args) {
        pattern(5);

    }
    static void pattern(int n){
        for (int i = 1; i <n ; n++) {
            for (int j = 1; j <n; n++) {
                System.out.print("n");

            }
            System.out.println();

        }
    }

}
